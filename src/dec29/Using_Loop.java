package dec29;

public class Using_Loop {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
		{
			System.out.println("manasa::"+i);
		}
		System.out.println("==========");
		for(int i=1;i<=5;i+=3)
		{
			System.out.println("manasa::"+i);
		}
		System.out.println("=======");
		for(int i=10;i>=1;i--)
		{
			System.out.println("manasa::"+i);
		}

	}

}
